package 문제풀이;

public class 문제풀이5 {
	
	public static void main(String[] args) {
		
		int korean = 75;
		int math = 50;
		
		// 점수의 총합
		int total = korean + math;
		System.out.println("총 점수는 "+total +"점입니다.");
		
		//평균
		double average = (double)total/2;
		System.out.println("평균 점수는 "+average+"점입니다.");
		
	}

}
